    public void fillTable(List<NhanVien_Model> list) {
        
        defaultTableModel =  (DefaultTableModel) tbl_nhanVien.getModel();
        defaultTableModel.setRowCount(0);
        for (NhanVien_Model nhanVien_Model : list) {
            defaultTableModel.addRow(new Object[]{
                nhanVien_Model.getMaNV(),nhanVien_Model.getMaTK(),
                nhanVien_Model.getHoTen(),nhanVien_Model.getGioiTinh(),
                nhanVien_Model.getDiaChi(),nhanVien_Model.getSdt(),nhanVien_Model.getTrangThai()
            });
        }
    }
    void showData(int index){
        NhanVien_Model nv = nhanVienService.getAll().get(index);
        txt_maNV.setText(nv.getMaNV());
        txt_maTK.setText(nv.getMaTK());
        txt_hoTen.setText(nv.getHoTen());
        if(nv.getGioiTinh()==1){
            rd_nam.setSelected(true);
        }else{
            rd_nu.setSelected(true);
        }
        txt_diaChi.setText(nv.getDiaChi());
        txt_SDT.setText(nv.getSdt());
        if (nv.getTrangThai()==1) {
            rd_hoatDong.setSelected(true);
        } else {
            rd_khongHoatDong.setSelected(true);
        }
    }
    private NhanVien_Model readform(){
       int gioiTinh,trangThai;
       String MaNV = txt_maNV.getText();
       String MaTK = txt_maTK.getText();
       String hoTen = txt_hoTen.getText();
       if(rd_nam.isSelected()){
           gioiTinh=1;
       }else{
           gioiTinh=0;
       }
       String diaChi = txt_diaChi.getText();
       String sdt = txt_SDT.getText();
       if(rd_hoatDong.isSelected()){
           trangThai=1;
       }else{
           trangThai=0;
       } 
       return new NhanVien_Model(MaNV, MaTK, hoTen, gioiTinh, diaChi, sdt, trangThai);
    }

    boolean checktrung(){
        List<NhanVien_Model> nvM = nhanVienService.getAll();
        for (int i = 0; i < nvM.size(); i++) {
            if(txt_maNV.getText().equalsIgnoreCase(nvM.get(i).getMaNV())){
                JOptionPane.showMessageDialog(this, "trung ma");
                return false;
            }
        }
        return true;
    }
    boolean Validate(){
        String sdt = txt_SDT.getText();
        String checksdt = "0\\d{9}";
        if(txt_maNV.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "ma nhan vien trong");
            return false;
        }
          if(txt_maTK.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "ma tai khoan trong");
            return false;
        }
            if(txt_hoTen.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "ho ten trong");
            return false;
        }
              if(txt_diaChi.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "dia chi trong");
            return false;
        }
                if(txt_SDT.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "so dien thoai trong");
            return false;
        }
                if(!sdt.matches(checksdt)){
                    JOptionPane.showMessageDialog(this, "dinh dang sdt sai");
                    return  false;
                }
            return true;
    }
